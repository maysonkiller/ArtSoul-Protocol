-- A8c Protocol Admin review workflow (additive, feature-flagged).
--
-- This migration is not an activation instruction. Apply it only through the
-- reviewed A8 rollout runbook. Browser roles cannot read complaint, audit, or
-- notification data; the server uses service_role after SIWE + role + passkey.

ALTER TABLE public.artwork_reports
    ADD COLUMN IF NOT EXISTS reviewed_by TEXT,
    ADD COLUMN IF NOT EXISTS reviewed_at TIMESTAMPTZ,
    ADD COLUMN IF NOT EXISTS decision_reason TEXT;

ALTER TABLE public.artwork_reports
    DROP CONSTRAINT IF EXISTS artwork_reports_reviewed_by_check;
ALTER TABLE public.artwork_reports
    ADD CONSTRAINT artwork_reports_reviewed_by_check CHECK (
        reviewed_by IS NULL OR (
            reviewed_by = LOWER(reviewed_by) AND
            reviewed_by ~ '^0x[0-9a-f]{40}$'
        )
    );

ALTER TABLE public.artwork_reports
    DROP CONSTRAINT IF EXISTS artwork_reports_decision_reason_check;
ALTER TABLE public.artwork_reports
    ADD CONSTRAINT artwork_reports_decision_reason_check CHECK (
        decision_reason IS NULL OR (
            LENGTH(TRIM(decision_reason)) > 0 AND
            LENGTH(decision_reason) <= 500
        )
    );

ALTER TABLE public.artwork_report_events
    ADD COLUMN IF NOT EXISTS reason TEXT;
ALTER TABLE public.artwork_report_events
    DROP CONSTRAINT IF EXISTS artwork_report_events_event_type_check;
ALTER TABLE public.artwork_report_events
    ADD CONSTRAINT artwork_report_events_event_type_check CHECK (event_type IN (
        'REPORT_SUBMITTED',
        'REPORT_HIDDEN',
        'REPORT_DISMISSED',
        'REPORT_REOPENED',
        'REPORT_RESOLVED',
        'REPORT_RESTORED',
        'NOTIFICATION_QUEUED'
    ));
ALTER TABLE public.artwork_report_events
    DROP CONSTRAINT IF EXISTS artwork_report_events_reason_check;
ALTER TABLE public.artwork_report_events
    ADD CONSTRAINT artwork_report_events_reason_check CHECK (
        reason IS NULL OR (
            LENGTH(TRIM(reason)) > 0 AND
            LENGTH(reason) <= 500
        )
    );

CREATE TABLE IF NOT EXISTS public.artwork_report_notifications (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    report_id UUID NOT NULL REFERENCES public.artwork_reports(id) ON DELETE RESTRICT,
    recipient_wallet TEXT NOT NULL,
    notification_type TEXT NOT NULL CHECK (notification_type IN (
        'REPORT_ACTIONED',
        'REPORT_DISMISSED',
        'REPORT_REOPENED',
        'ARTWORK_HIDDEN',
        'ARTWORK_RESTORED'
    )),
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    read_at TIMESTAMPTZ,
    CHECK (recipient_wallet = LOWER(recipient_wallet)),
    CHECK (recipient_wallet ~ '^0x[0-9a-f]{40}$')
);

CREATE INDEX IF NOT EXISTS idx_artwork_report_notifications_recipient
    ON public.artwork_report_notifications (recipient_wallet, created_at DESC);
CREATE INDEX IF NOT EXISTS idx_artwork_report_notifications_report
    ON public.artwork_report_notifications (report_id, created_at DESC);

ALTER TABLE public.artwork_reports ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.artwork_reports FORCE ROW LEVEL SECURITY;
ALTER TABLE public.artwork_report_events ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.artwork_report_events FORCE ROW LEVEL SECURITY;
ALTER TABLE public.artwork_report_notifications ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.artwork_report_notifications FORCE ROW LEVEL SECURITY;

REVOKE ALL ON public.artwork_reports FROM PUBLIC, anon, authenticated;
REVOKE ALL ON public.artwork_report_events FROM PUBLIC, anon, authenticated;
REVOKE ALL ON public.artwork_report_notifications FROM PUBLIC, anon, authenticated;
